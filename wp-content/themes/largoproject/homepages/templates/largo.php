		<p class="intro"><strong>Project Largo</strong> is a WordPress starter/parent theme developed by the <a href="http://investigativenewsnetwork.org">Investigative News Network</a>. Sound interesting? <a href="https://github.com/INN/Largo">Check out our code on Github</a> or <a href="/contact/">get in touch</a> to setup a demo.</p>

		<p>Largo incorporates a number of features from NPR's <a href="http://argoproject.org">Project Argo</a> but has been significantly expanded to include a number of additional features to make it easier to set up and more flexible to better support the needs of a variety of news publishers.</p>

		<p><strong>Status:</strong> We now have over 20 INN member sites and over 30 non-member sites using Largo and we're continuing to improve the platform every day! <a href="/contact/">Drop us a note</a> if you'd like to receive updates on our progress or are interested in contributing code to the project.</p>

		<h3>Key Features</h3>

		<p><strong>Responsive design</strong> to accommodate a variety of devices, screen sizes and resolutions</p>

		<p><strong>Flexible homepage layout options</strong> (blog, newspaper and carousel) to support different news site presentation needs <span class="small">(Click to view larger)</span></p>


